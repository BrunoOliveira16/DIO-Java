public class TiposVariaveis {

    public static void main(String[] args) {

        int anoFabricacao = 2023;
        float salarioMinimo = 1300f; // precisa terminar com f
        double salario = 1275.33;
        long cpf = 98711122286l; // precisa terminar com l

        System.out.println(anoFabricacao);
        System.out.println(salarioMinimo);
        System.out.println(salario);
        System.out.println(cpf);

        int idade = 20;
        idade = 21;
        System.out.println(idade);

        // constante (formas de representar)
        final float pi = 3.14f;
        float VALOR_DE_PI = 3.14F;

        System.out.println(pi);
        System.out.println(VALOR_DE_PI);
    }
}
